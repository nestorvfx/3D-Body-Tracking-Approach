# --------------------------------------------------------------------------
# Dataset plan — MAXIMUM diversity strategy.
#
# Problem with a fixed hand-written clip list: walks × 4 seeds look similar
# because they're ALL walking.  Solution: at build time we SAMPLE clips
# randomly from the full asset pool, giving exposure to:
#   - 10 CMU clips (different subjects, different activities)
#   - 100STYLE's 100 STYLE MOTION CATEGORIES × 5 variants (BR/BW/FR/FW/ID)
#     — each category is a VISUALLY DISTINCT motion (Angry, Crouched,
#     Chicken, Dinosaur, Jump, Kick, Lunge, Penguin, Punch, Robot, Skip,
#     Sweep, Teapot, Tiptoe, Waving, Zombie, etc.)
#   - AIST++ 10 dance genres × many clips (gBR breakdance, gHO house,
#     gJB jazz ballet, gJS jazz, gKR krumping, gLH LA-hiphop, gLO locking,
#     gMH mid-hiphop, gPO pop, gWA waacking)
#
# We build a deterministic per-run plan that:
#   - Samples N_CLIPS clips uniformly from all available BVHs, with the
#     constraint that each of the three source families gets at least 30%
#     of the budget (no source dominates).
#   - Picks random FRAMES across each clip's range (the retarget's
#     find_worst_frames also contributes extreme bends).
#   - Uses a 30-seed pool so every character is visually distinct.
#
# The PLAN variable is now generated by build_plan() below.
# --------------------------------------------------------------------------

# Wider seed pool → each (clip, seed) pair is a unique character.  With 100
# seeds we can build up to 100 distinct phenotypes; above that we cycle with
# slightly different clothing / HDRI combos which is fine for diversity.
SEED_POOL = list(range(42, 142))          # 100 distinct seeds


def build_plan(rng: random.Random, n_clips: int,
               seeds_per_clip: int, frames_per_clip: int
               ) -> list[tuple[str, list[int], int, int]]:
    """Sample ``n_clips`` diverse clips from the asset pool.

    For 100k-sample runs n_clips reaches ~10k, far more than the available
    unique clips (~1200).  We therefore sample *with replacement* once the
    unique pool is exhausted, pairing each repeat with a different seed and
    different frame subset — so downstream images still differ.
    """
    assets = HERE.parent / "assets"
    cmu = sorted((assets / "bvh").glob("*.bvh"))
    style = sorted((assets / "bvh_100style" / "100STYLE").rglob("*.bvh"))
    aist = sorted((assets / "aist_plusplus" / "bvh").glob("*.bvh"))

    # Target per-source shares: CMU ~10 %, AIST ~35 %, 100STYLE ~55 %
    # (100STYLE dominates because it's the widest motion-category pool).
    # For very small n_clips (smoke tests) allow 0 in any bucket.
    if n_clips >= 3:
        n_cmu = max(1, int(round(n_clips * 0.10)))
        n_aist = max(1, int(round(n_clips * 0.35)))
        n_style = max(0, n_clips - n_cmu - n_aist)
    else:
        # Tiny budget → just round-robin across sources.
        alloc = [0, 0, 0]
        for i in range(n_clips):
            alloc[i % 3] += 1
        n_cmu, n_aist, n_style = alloc

    def _sample_pool(pool, k):
        if not pool:
            return []
        if k <= len(pool):
            return rng.sample(pool, k)
        # Draw without replacement first (full pool), then fill with
        # replacement — each repeat gets a fresh seed+frame downstream.
        out = list(pool)
        rng.shuffle(out)
        while len(out) < k:
            out.append(rng.choice(pool))
        return out

    picks = []
    picks += _sample_pool(cmu, n_cmu)
    # For 100STYLE, sample uniformly over *styles* first to maximise
    # motion-type diversity, then within each style pick one variant.
    style_by_dir: dict[str, list[Path]] = {}
    for p in style:
        style_by_dir.setdefault(p.parent.name, []).append(p)
    style_keys = list(style_by_dir.keys())
    chosen_styles = _sample_pool(style_keys, n_style)
    picks += [rng.choice(style_by_dir[s]) for s in chosen_styles]
    picks += _sample_pool(aist, n_aist)
    rng.shuffle(picks)

    plan = []
    for p in picks:
        seeds = rng.sample(SEED_POOL, min(seeds_per_clip, len(SEED_POOL)))
        rel = p.relative_to(assets).as_posix()
        plan.append((rel, seeds, frames_per_clip, 1))
    return plan

MASTER_SEED = 20260420   # per PIPELINE_DESIGN
IMAGE_W = 256
IMAGE_H = 192


def sample_frame_indices(ctx: RetargetContext, n: int, rng: random.Random) -> list[int]:
    """Random frames spread across the full clip range (not uniform), for
    pose diversity across the clip rather than periodic walk-cycle phase."""
    start, end = ctx.frame_range
    if end <= start or n <= 0:
        return [start]
    # Sample without replacement if the range is large enough.
    if end - start + 1 >= n * 2:
        return sorted(rng.sample(range(start, end + 1), n))
    # Fall back to evenly spaced.
    step = max(1, (end - start) // max(1, n - 1))
    return list(range(start, end + 1, step))[:n]


def split_for_id(sample_id: str) -> str:
    """Deterministic 90/10 train/val split by id hash."""
    h = int(hashlib.sha1(sample_id.encode()).hexdigest()[:2], 16)   # 0..255
    return "val" if h < 26 else "train"     # ~10% val


def compute_extrinsics(camera) -> dict:
    """Return R (world->camera) and t, in OpenCV convention (y down, z fwd).
    Blender's camera looks along -Z_world; flip Y and Z to match OpenCV.
    """
    mw = camera.matrix_world
    R_w2c_blender = mw.to_3x3().inverted()
    t_w2c_blender = -(R_w2c_blender @ mw.to_translation())
    flip = mathutils.Matrix([[1, 0, 0], [0, -1, 0], [0, 0, -1]])
    R_cv = flip @ R_w2c_blender
    t_cv = flip @ t_w2c_blender
    return {
        "R": [list(R_cv.row[0]), list(R_cv.row[1]), list(R_cv.row[2])],
        "t": list(t_cv),
    }


def world_to_camera_3d(points_world, camera):
    """Return camera-frame 3D coords (OpenCV convention)."""
    extr = compute_extrinsics(camera)
    R = mathutils.Matrix(extr["R"])
    t = mathutils.Vector(extr["t"])
    out = []
    for p in points_world:
        if p is None:
            out.append(None)
            continue
        wp = mathutils.Vector(p)
        cam_p = R @ wp + t
        out.append([float(cam_p.x), float(cam_p.y), float(cam_p.z)])
    return out


def tight_bbox(kps_2d_pixels, visibility, pad: float = 1.25, W=IMAGE_W, H=IMAGE_H):
    xs, ys = [], []
    for (kp, v) in zip(kps_2d_pixels, visibility):
        if v <= 0:
            continue
        u, vv = kp
        if 0 <= u < W and 0 <= vv < H:
            xs.append(u); ys.append(vv)
    if not xs:
        return [0, 0, W, H]
    x_min, x_max = min(xs), max(xs)
    y_min, y_max = min(ys), max(ys)
    w = max(1.0, x_max - x_min)
    h = max(1.0, y_max - y_min)
    cx, cy = x_min + w/2, y_min + h/2
    w *= pad; h *= pad
    x = max(0.0, cx - w/2)
    y = max(0.0, cy - h/2)
    w = min(W - x, w)
    h = min(H - y, h)
    return [int(x), int(y), int(w), int(h)]


def _parse_args() -> argparse.Namespace:
    """Parse arguments after the Blender ``--`` separator.

    Backwards-compatible with the old positional invocation
    ``blender ... -- <out_dir> [n_override]``.
    """
    argv = sys.argv[sys.argv.index("--") + 1:] if "--" in sys.argv else []
    p = argparse.ArgumentParser()
    p.add_argument("--out", default="dataset/output/synth_v1",
                   help="output directory (default: dataset/output/synth_v1)")
    p.add_argument("--target-count", type=int, default=1000,
                   help="total sample budget (default: 1000)")
    p.add_argument("--seeds-per-clip", type=int, default=2,
                   help="distinct characters per clip (default: 2)")
    p.add_argument("--frames-per-clip", type=int, default=5,
                   help="frames sampled per (clip, seed) pair (default: 5)")
    p.add_argument("--limit", type=int, default=None,
                   help="hard cap on total samples rendered this run")
    # Legacy positional support: if first token is not a flag, treat it as --out.
    if argv and not argv[0].startswith("-"):
        argv = ["--out", argv[0]] + (
            ["--limit", argv[1]] if len(argv) > 1 else [])
    return p.parse_args(argv)


def main():
    args = _parse_args()
    # Make out_dir absolute — Blender's internal render path handling can
    # silently drop renders when given relative paths depending on CWD.
    out_dir = Path(args.out).resolve()
    limit = args.limit
    target_count = args.target_count
    seeds_per_clip = args.seeds_per_clip
    frames_per_clip = args.frames_per_clip
    n_clips = max(1, math.ceil(target_count / (seeds_per_clip * frames_per_clip)))
    out_dir.mkdir(parents=True, exist_ok=True)
    (out_dir / "images").mkdir(exist_ok=True)
    labels_path = out_dir / "labels.jsonl"
    manifest_path = out_dir / "manifest.csv"

    # Resume support: append to labels + skip already-rendered images.
    done_ids: set[str] = set()
    if labels_path.exists():
        with labels_path.open() as fh:
            for line in fh:
                try:
                    done_ids.add(json.loads(line)["id"])
                except Exception:
                    pass
        print(f"[resume] {len(done_ids)} samples already rendered")

    rng_master = random.Random(MASTER_SEED)

    # Pick HDRI from available pool (currently just empty_warehouse).
    hdris = sorted((HERE.parent / "assets" / "hdris").glob("*.hdr"))
    if not hdris:
        print("no HDRIs found in dataset/assets/hdris/")
        sys.exit(1)

    stats = {"cmu": 0, "100style": 0, "aistpp": 0, "total": 0,
             "train": 0, "val": 0}

    labels_fh = labels_path.open("a", encoding="utf-8")
    manifest_fh = manifest_path.open(
        "w" if not manifest_path.exists() or not done_ids else "a",
        encoding="utf-8", newline="")
    if not done_ids:
        manifest_fh.write("id,split,source,image_rel,bbox_x,bbox_y,bbox_w,bbox_h\n")

    # Build diverse plan at run time (deterministic via MASTER_SEED).
    plan = build_plan(random.Random(MASTER_SEED), n_clips,
                      seeds_per_clip, frames_per_clip)
    total_planned = sum(len(s) * n * c for _, s, n, c in plan)
    print(f"[plan] {len(plan)} clips  → {total_planned} samples target "
          f"(budget: {target_count})")

    sample_counter = 0
    t_run_start = time.time()
    for plan_i, (bvh_rel, seeds, n_frames, cams_per) in enumerate(plan):
        bvh_abs = HERE.parent / "assets" / bvh_rel
        if not bvh_abs.exists():
            print(f"[skip] missing BVH {bvh_abs}")
            continue
        src_kind = detect_source_from_bvh(str(bvh_abs))
        clip_id = bvh_abs.stem

        for seed in seeds:
            clear_scene()
            bm, arm = build_character(seed, with_assets=True)
            bvh = load_bvh(str(bvh_abs), source=src_kind)
            ctx = RetargetContext(bvh, arm)

            # Camera object (reused within the scene; intrinsics vary).
            cam = crig.make_camera("DatasetCam")
            bpy.context.scene.camera = cam
            configure_render(
                resolution=(IMAGE_W, IMAGE_H),
                engine="BLENDER_EEVEE", samples=32,
                output_path=str(out_dir / "images" / "_.png"))
            cam.rotation_mode = "XYZ"

            rng_clip = random.Random((seed * 100003) ^ hash(bvh_rel))
            frames = sample_frame_indices(ctx, n_frames, rng_clip)

            for frame in frames:
                ctx.apply_pose(frame)
                bpy.context.view_layer.update()

                for cam_i in range(cams_per):
                    sample_id = f"{src_kind}_{clip_id}_f{frame:04d}_s{seed}_c{cam_i}"
                    img_path = out_dir / "images" / f"{sample_id}.png"
                    if sample_id in done_ids:
                        continue

                    # PER-SAMPLE HDRI (scene lighting diversity) — rotate by id hash.
                    hdri_idx = int(hashlib.sha1(sample_id.encode()).hexdigest()[:4], 16) % len(hdris)
                    set_world_hdri(str(hdris[hdri_idx]),
                                   strength=rng_clip.uniform(0.7, 1.6))

                    # Per-sample RNG (deterministic).
                    rng_cam = random.Random(
                        rng_master.randrange(1 << 30) ^ (frame << 8) ^ cam_i)
                    try:
                        focal = crig.sample_focal_mm(rng_cam)
                        cam_sample = crig.frame_armature_strict(
                            cam, arm, rng_cam, focal_mm=focal)
                    except Exception as e:
                        print(f"  [skip cam] {sample_id}: {e}")
                        continue
                    bpy.context.view_layer.update()

                    bpy.context.scene.render.filepath = str(img_path)
                    bpy.ops.render.render(write_still=True)

                    # Compute labels
                    kps3d_world = get_coco17_world(arm)
                    proj = project_to_pixels(
                        bpy.context.scene, cam, kps3d_world)
                    kps2d_pixels = []
                    vis_flags = []
                    for kp in proj:
                        if kp is None:
                            kps2d_pixels.append([0.0, 0.0])
                            vis_flags.append(0)
                            continue
                        u, v, depth, inside = kp
                        px, py = u * IMAGE_W, v * IMAGE_H
                        kps2d_pixels.append([float(px), float(py)])
                        vis_flags.append(2 if inside else 1)

                    kps3d_cam = world_to_camera_3d(kps3d_world, cam)
                    intr_dict = cam_sample.intrinsics.K()
                    extrinsics = compute_extrinsics(cam)
                    bbox = tight_bbox(kps2d_pixels, vis_flags)
                    split = split_for_id(sample_id)

                    # Root joint = mean of hip keypoints (COCO-17 indices 11, 12).
                    root = None
                    if kps3d_cam[11] is not None and kps3d_cam[12] is not None:
                        l, r = kps3d_cam[11], kps3d_cam[12]
                        root = [(l[0]+r[0])/2, (l[1]+r[1])/2, (l[2]+r[2])/2]

                    record = {
                        "id": sample_id,
                        "split": split,
                        "image_rel": f"images/{sample_id}.png",
                        "image_wh": [IMAGE_W, IMAGE_H],
                        "camera_K": intr_dict,
                        "camera_extrinsics": extrinsics,
                        "bbox_xywh": bbox,
                        "keypoints_2d": [
                            [kp[0], kp[1], vf]
                            for kp, vf in zip(kps2d_pixels, vis_flags)
                        ],
                        "keypoints_3d_cam": kps3d_cam,
                        "root_joint_cam": root,
                        "source": src_kind,
                        "clip_id": clip_id,
                        "frame_idx": frame,
                        "character_seed": seed,
                        "hdri": hdris[hdri_idx].stem,
                        "focal_mm": float(cam_sample.intrinsics.focal_mm),
                        "render_engine": "BLENDER_EEVEE",
                    }
                    labels_fh.write(json.dumps(record) + "\n")
                    labels_fh.flush()
                    manifest_fh.write(
                        f"{sample_id},{split},{src_kind},{record['image_rel']},"
                        f"{bbox[0]},{bbox[1]},{bbox[2]},{bbox[3]}\n")
                    manifest_fh.flush()

                    stats["total"] += 1
                    stats[src_kind] = stats.get(src_kind, 0) + 1
                    stats[split] += 1
                    sample_counter += 1

                    # Live progress: samples/sec and ETA — Blender's Python
                    # doesn't reliably redraw \r, so print a fresh line.
                    if sample_counter % 25 == 0:
                        elapsed = max(1e-3, time.time() - t_run_start)
                        rate = sample_counter / elapsed
                        remaining = max(0, target_count - stats["total"])
                        eta_s = int(remaining / rate) if rate > 0 else -1
                        eta_str = (f"{eta_s//3600:d}h{(eta_s%3600)//60:02d}m"
                                     if eta_s >= 0 else "?")
                        print(f"  [{stats['total']:6d}/{target_count}] "
                              f"{rate:4.2f} samples/s  ETA {eta_str}  "
                              f"({sample_id})")
                    if limit is not None and stats["total"] >= limit:
                        print(f"[limit] reached {limit}, stopping")
                        labels_fh.close(); manifest_fh.close()
                        return
                    if stats["total"] >= target_count:
                        print(f"[target] reached {target_count}, stopping")
                        labels_fh.close(); manifest_fh.close()
                        return

    labels_fh.close()
    manifest_fh.close()

    # Stats doc
    with (out_dir / "dataset_stats.md").open("w") as fh:
        fh.write(f"# Dataset stats (synth_v1)\n\n")
        fh.write(f"Total samples: {stats['total']}\n\n")
        fh.write(f"Per source:\n")
        for src in ("cmu", "100style", "aistpp"):
            fh.write(f"- {src}: {stats.get(src, 0)}\n")
        fh.write(f"\nSplit:\n")
        fh.write(f"- train: {stats['train']}\n")
        fh.write(f"- val: {stats['val']}\n")
        fh.write(f"\nImage size: {IMAGE_W}x{IMAGE_H}\n")
        fh.write(f"Master seed: {MASTER_SEED}\n")

    print(f"[done] wrote {stats['total']} samples to {out_dir}")


if __name__ == "__main__":
    main()
