"""Build a synthetic 3D-pose dataset (scalable from 1k to 100k+ samples).

Produces:
  <out>/images/<id>.png             rendered 256x192 RGB
  <out>/labels.jsonl                one JSON per sample
  <out>/manifest.csv                compact index for training
  <out>/dataset_stats.md            per-source counts + split

Each sample includes:
  camera intrinsics K (fx, fy, cx, cy)
  camera extrinsics (world -> camera) R, t
  COCO-17 2D keypoints (u, v, visibility_flag)
  COCO-17 3D keypoints in camera frame (metres)
  bbox_xywh (tight +25 %)
  source / clip / frame / seed / phenotype summary

Usage (Blender's Python):
  blender --background --python build_dataset.py -- \\
      --out dataset/output/synth_v2 \\
      --target-count 100000 \\
      --seeds-per-clip 2 \\
      --frames-per-clip 5
"""
